# Dynamic typing in Ruby means that you don't need to specify the type of a variable when you declare it.
# The type is determined at runtime based on the value assigned to the variable. 
# This allows flexibility in handling different types of data without requiring explicit type definitions.

x = 10      # x is an integer
x = "Hello" # x is now a string

# In Ruby, the same variable x can hold different types of data during the program's execution,
# and the type is decided dynamically as the value changes.